package deco2800.arcade.wl6;

public class Player {

	private float x = 0;
	private float y = 0;
	private float angle = 0;
	

	public Player() {
		super();
		
	}
	
	
	public float getX() {
		return x;
	}



	public void setX(float x) {
		this.x = x;
	}



	public float getY() {
		return y;
	}



	public void setY(float y) {
		this.y = y;
	}



	public float getAngle() {
		return angle;
	}



	public void setAngle(float angle) {
		this.angle = angle;
	}



	
	
}
